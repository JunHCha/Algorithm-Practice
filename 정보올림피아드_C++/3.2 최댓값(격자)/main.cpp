#include <iostream>

using namespace std;

int map[9][9], maxNum, x, y;

int main() {
	for (int i = 0; i < 9; i++) {
		for (int j = 0; j < 9; j++) {
			cin >> map[i][j];
		}
	}

	for (int i = 0; i < 9; i++) {
		for (int j = 0; j < 9; j++) {
			if (map[i][j] > maxNum) {
				maxNum = map[i][j];
				x = i + 1;
				y = j + 1;
			}
		}
	}
	cout << maxNum << endl;
	cout << x << " " << y;
	 
	return 0;
}


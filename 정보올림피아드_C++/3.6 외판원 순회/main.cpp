#include <iostream>

using namespace std;

int n, w[10][10], visited[10], res, cnt = 0, minNum = 100000000;

void input() {
	cin >> n;
	for (int i = 0; i < n; i++)
		for (int j = 0; j < n; j++)
			cin >> w[i][j];
}

void dfs( int vertex) {
	visited[vertex] = 1;
	cnt++;


	if (cnt == n && w[vertex][0]) {
		res += w[vertex][0];
		if (res < minNum)
			minNum = res;
		res -= w[vertex][0];
		return;
	}
	
	for (int i = 0; i < n; i++)	{
		if (!visited[i] && w[vertex][i])	{
			visited[i] = 1;
			res += w[vertex][i];
			
			dfs(i);

			res -= w[vertex][i];
			cnt--;
			visited[i] = 0;		
		}
	}
}

int main() {
	input();
	dfs(0);
	cout << minNum;
	 
	return 0;
}

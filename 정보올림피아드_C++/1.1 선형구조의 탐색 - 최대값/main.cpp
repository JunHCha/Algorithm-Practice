#include <iostream>

#define MIN_INT -10001

using namespace std;

int maxNum, ans, container[10];

int main() {
	maxNum = MIN_INT;
	ans = -1;
	for (int i = 0; i < 9; i++) {
		cin >> container[i];
	}
	for (int i = 0; i < 9; i++) {
		if (container[i] > maxNum) {
			maxNum = container[i];
			ans = i;
		}
	}
	cout << maxNum << endl;
	cout << ans + 1;

	 
	return 0;
}
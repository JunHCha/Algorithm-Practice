#include <iostream>

using namespace std;

int n, cnt;

void solve(int val) {
	for (int a = 1; a < val; a++) {
		for (int b = a; b < val - a; b++) {
			int c = val - (a + b);
			if (c >= b && a + b > c) cnt++;
		}
	}
}

int main() {
	cin >> n;
	solve(n);
	cout << cnt;
	 
	return 0;
}